package nl.michielgraat.adventofcode2018.day17;

public class Line {
    Integer x1;
    Integer x2;
    Integer y1;
    Integer y2;

    public Line(final Integer x1, final Integer x2, final Integer y1, final Integer y2) {
        this.x1 = x1;
        this.x2 = x2;
        this.y1 = y1;
        this.y2 = y2;
    }
}